new Vue({
    el:"#app",
    data:{
      foodname:"",
      pic:"",
      foodfunction:"",
      coke:"",
      diffficult:"",
      member:"",
      taste:"",
      readytime:"",
      coketime:"",
      author:"",
      details:"",
      making:"",
      support:"",
      teps:"" 
    },
    created(){
        axios.get(
            "http://127.0.0.1:8080/fid/list_fid",
            { 
                params:{fid:location.search.split("=")[1]}
            }
        ).then(result=>{
            console.log(result.data);
            var r=result.data;
            this.foodname=r.foodname;
            this.pic=r.pic;
            this.foodfunction=r.foodfunction.split("|");
            console.log(this.foodfunction);
            this.coke=r.coke;
            this.diffficult=r.diffficult;
            this.member=r.member;
            this.taste=r.taste;
            this.readytime=r.readytime;
            this.coketime=r.coketime;
            this.author=r.author;
            this.details=r.details;
            this.making=r.making.split("|");
            console.log(this.making);
            this.support=r.support.split("|");
            console.log(this.support);
            this.teps=r.teps.split("|");
            console.log(this.teps);
        });
    }
})